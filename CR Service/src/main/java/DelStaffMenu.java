    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String line = null,word;
        int i;
        StringTokenizer st = null;
        String update = jText_id.getText();
        Integer id = Integer.parseInt(update);
        File file = new File("DelStaffReg.txt");
        File newfile = new File("NewDelStaffReg.txt");
        if(!file.exists()){
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(DelStaffMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        try{

            Scanner scan = new Scanner(file);
            FileWriter fw = new FileWriter(newfile);
            BufferedWriter bw = new BufferedWriter(fw);
            while(scan.hasNextLine())
            {

                line = scan.nextLine();
                st = new StringTokenizer(line,",");
                i = 0;
                while(st.hasMoreTokens())
                {

                    word = st.nextToken();
                    if(i == 0)
                    {
                        if(!update.equals(word)){
                            bw.write(line);
                            bw.flush();
                            bw.newLine();
                        }
                        else{
                            String sid = jText_id.getText();
                            String fname = jText1.getText();
                            String lname = jText2.getText();
                            String address = jText3.getText();
                            String phone = jText4.getText();
                            String usrname = jText5.getText();
                            String pw = jText6.getText();
                            bw.write(sid+","+fname+","+lname+","+address+","+phone+","+usrname+","+pw);
                            bw.flush();
                            bw.newLine();

                        }

                    }
                    i++;
                }

            }
            scan.close();
            bw.close();
            file.delete();
            newfile.renameTo(file);
            jText1.setText(null);
            jText2.setText(null);
            jText3.setText(null);
            jText4.setText(null);
            jText5.setText(null);
            jText6.setText(null);
            JOptionPane.showMessageDialog(null,"User data Updated");

        }

        catch(FileNotFoundException ex){
            Logger.getLogger(DelStaffMenu.class.getName()).log(Level.SEVERE,null,ex);
        }   catch (IOException ex) {
            Logger.getLogger(DelStaffMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

