    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        File file = new File("Order.txt");
        try{
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            Object[] lines = br.lines().toArray();
            for(int i =0; i <lines.length;i++){
                String[] row = lines[i].toString().split(",");
                model.addRow(row);
            }

        }
        catch(FileNotFoundException ex){
            Logger.getLogger(OrderManagement.class.getName()).log(Level.SEVERE,null,ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int a = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        if(a >=0)
        {
            model.setValueAt(jText4.getText(), a, 1);
            model.setValueAt(jText5.getText(), a, 2);
            model.setValueAt(jText6.getText(), a, 3);
            model.setValueAt(jText7.getText(), a, 4);
            model.setValueAt(jText8.getText(), a, 5);
            model.setValueAt(jText9.getText(), a, 6);
            model.setValueAt(jText11.getText(), a, 7);
            
            JOptionPane.showMessageDialog(null,"Updated Successfully");
            jText4.setText(null);
            jText5.setText(null);
            jText6.setText(null);
            jText7.setText(null);
            jText8.setText(null);
            jText9.setText(null);
            jText11.setText(null);
            File file = new File("Order.txt");
            try {
                FileWriter fw = new FileWriter(file);
                BufferedWriter bw = new BufferedWriter(fw);
                for(int i=0; i< jTable1.getRowCount();i++){    
                for(int j=0; j< jTable1.getColumnCount(); j++){
                    bw.write(jTable1.getValueAt(i,j).toString()+",");
                    }
                    bw.newLine();   
                }
                bw.close();
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(OrderManagement.class.getName()).log(Level.SEVERE, null, ex);
            }
            }
        
        else{
            JOptionPane.showMessageDialog(null,"Error");
        }


    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int a = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        if(a >= 0){
            model.removeRow(a);
            JOptionPane.showMessageDialog(null,"Removed Successfully");

        }
        else{
            JOptionPane.showMessageDialog(null,"Error Occured");
        }
        File file = new File("Order.txt");
        try{
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            for(int i=0; i< jTable1.getRowCount();i++){
                for(int j=0; j< jTable1.getColumnCount(); j++){
                    bw.write(jTable1.getValueAt(i,j).toString()+",");
                }
                bw.newLine();
            }
            bw.close();
            fw.close();

        } catch (IOException ex) {
            Logger.getLogger(OrderManagement.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

