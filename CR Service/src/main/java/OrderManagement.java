    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String sid = this.jText2.getText();
        String ordid = this.jText3.getText();
        String name = this.jText4.getText();
        String date = this.jText5.getText();
        String weight = this.jText6.getText();
        String pick = this.jText7.getText();
        String drop = this.jText8.getText();
        String type = this.jText9.getText();
        String status = this.jText10.getText();
        String amount = this.jText11.getText();
        File file = new File("Delivery.txt");
        if(!file.exists()){
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(OrderManagement.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    try {   
        FileWriter fw = new FileWriter(file,true);
        BufferedWriter bw = new BufferedWriter(fw);
        bw.write(sid+","+ordid+","+name+","+date+","+weight+","+pick+","+drop+","+type+","+status+","+amount+"\n");
        bw.flush();
        bw.close();
        JOptionPane.showMessageDialog(null, "Order assigned successfully");
        jText2.setText(null);
        jText3.setText(null);
        jText4.setText(null);
        jText6.setText(null);
        jText7.setText(null);
        jText8.setText(null);
        jText9.setText(null);
        jText11.setText(null);
        } catch (IOException ex) {
            Logger.getLogger(OrderManagement.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

